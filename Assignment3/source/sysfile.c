// we need to use this function to search if there is a free vma
int vma_searching(int bytes_number, struct proc *p) {
    for (int i = 0; i < VMASIZE; i++) {
        if (p->vma[i].mapped_file == 0 && p->vma[i].mapped_length == 0) {
            return i; // Empty VMA slot found.
        }
    }
    return -1; // No empty VMA slot found.
}

// get file from fd and increment reference by one since we need to use it outside this function
struct file* get_file_from_fd(int fd, struct proc *process) {
    if (fd < 0 || fd >= NOFILE || process->ofile[fd] == 0)
        return 0; // Invalid file descriptor or file is not open.

    acquire(&process->lock);
    struct file *f = process->ofile[fd];
    if(f)
        filedup(f); // Increment the ref count if you're going to use it outside of this context.
    release(&process->lock);

    return f; // Return the file pointer.
}
// we need to generate corresponding memory flgas according to the frame it send
int memory_flag_generator(int map_flags, int shared_flags){
  int page_flags = PTE_U|PTE_V;
  if (map_flags&0x1){
    page_flags = page_flags|PTE_R;
  }
  if (map_flags&0x2){
    page_flags = page_flags|PTE_W;
  }
  if (map_flags&0x4){
    page_flags = page_flags|PTE_X;
  }

  return page_flags;
}
// we need to use this function to set vma slot accordingly
void set_vma( 
              int vma_number, 
              int mapped_length, 
              int memory_flags, 
              int shared_flags, 
              int offset, 
              struct file *mapped_file, 
              struct proc *p) {
    acquire(&p->lock); // Assuming xv6 style lock for the process table.
    p->vma[vma_number].mapped_file = mapped_file;
    p->vma[vma_number].mapped_length = mapped_length;
    p->vma[vma_number].memory_flags = memory_flag_generator(memory_flags, shared_flags);
    p->vma[vma_number].mapped_flags = shared_flags;
    p->vma[vma_number].mapped_starting_address = (uint64)MAXVA-2*PGSIZE - (vma_number + 1) * 8 * PGSIZE;
    p->vma[vma_number].offset = offset;
    p->vma[vma_number].used_length = 0;
    release(&p->lock);
}



uint64
sys_mmap(void)
{
  int virtual_or_physical, length, memory_flags, sharing_flags, fd, offset, empty_vma_slot;
  struct file *f;
  struct proc *p = myproc(); // we try to get the information of the process

  argint(0, &virtual_or_physical);
  argint(1, &length);
  argint(2, &memory_flags);
  argint(3, &sharing_flags);
  argint(4, &fd);
  argint(5, &offset);

  /*
    In here we want to use the memory just lower than trapframe,
    Hope there should be no conflicts
  */
  // first we need to find an empty vma slot
  empty_vma_slot=vma_searching(length, p);
  // get file structure and fileup
  f = get_file_from_fd(fd, p);
  if (f == 0){
    printf("loading file structure failed\n");
    return -1;
  }
  // we need to first check that if memory flags and file flags are corresponds
  if (sharing_flags == 1){
    if ((memory_flags&0x2)&&(!f->writable)){
      return -1;
    }
    if ((memory_flags&0x1)&&(!f->readable)){
      return -1;
    }
  }
  // then we need to set it

  set_vma(empty_vma_slot, length, memory_flags, sharing_flags, offset,f, p);
  // finally to the mapping
  return p->vma[empty_vma_slot].mapped_starting_address;
}

